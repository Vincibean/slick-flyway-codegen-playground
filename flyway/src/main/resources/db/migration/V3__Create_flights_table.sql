    year INTEGER,
    month INTEGER,
    dayOfMonth INTEGER,
    dayOfWeek INTEGER,
    departureTime VARCHAR(4),
    scheduledDepTime NUMERIC,
    arrivalTime VARCHAR(4),
    scheduledArrivalTime NUMERIC,
    uniqueCarrier VARCHAR(3),
    flightNum INTEGER,
    tailNum VARCHAR(7),
    actualElapsedTime VARCHAR(3),
    scheduledElapsedTime INTEGER,
    airTime VARCHAR(3),
    arrivalDelay VARCHAR(3),
    departureDelay VARCHAR(3),
    origin VARCHAR(3),
    dest VARCHAR(3),
    distance INTEGER,
    taxiInTime VARCHAR(3),
    taxiOutTime VARCHAR(3),
    cancelled INTEGER,
    cancellationCode VARCHAR(2),
    diverted INTEGER,
    carrierDelay VARCHAR(3),
    weatherDelay VARCHAR(3),
    nasDelay VARCHAR(3),
    securityDelay VARCHAR(3),
    lateAircraftDelay VARCHAR(3)
